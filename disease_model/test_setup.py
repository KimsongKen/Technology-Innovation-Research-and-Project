def test_severity_prediction():
    import joblib, numpy as np, re
    import pandas as pd

    lgbm_sev = joblib.load(os.path.join(MODELS_DIR, "lgbm_severity_model.pkl"))
    cat_sev  = joblib.load(os.path.join(MODELS_DIR, "catboost_severity_model.pkl"))
    sev_map  = joblib.load(os.path.join(MODELS_DIR, "severity_map.pkl"))

    # Reverse map for decoding: {0: 'Mild', 1: 'Moderate', 2: 'Severe'}
    inv_sev_map = {v: k for k, v in sev_map.items()}

    df = pd.read_csv(os.path.join(DATA_DIR, "saca_final_dataset_self.csv"))
    X  = df.drop(["diseases", "Severity"], axis=1)

    def clean_feature_names(columns):
        cleaned = []
        seen = set()
        for col in columns:
            c = re.sub(r"[^A-Za-z0-9_]+", "_", col).strip("_").lower()
            if not c: c = "feature"
            orig, i = c, 1
            while c in seen:
                c = f"{orig}_{i}"; i += 1
            seen.add(c)
            cleaned.append(c)
        return cleaned

    X.columns  = clean_feature_names(list(X.columns))
    sample     = X.iloc[[0]]

    lgb_sev_pred = inv_sev_map[lgbm_sev.predict(sample)[0]]
    cat_sev_pred = inv_sev_map[cat_sev.predict(sample).flatten()[0]]

    print(f"         LightGBM → {lgb_sev_pred} | CatBoost → {cat_sev_pred}", end=" ")
    assert lgb_sev_pred in ["Mild", "Moderate", "Severe"], "LGB severity invalid"
    assert cat_sev_pred in ["Mild", "Moderate", "Severe"], "CB  severity invalid"

test("Severity models predict correctly", test_severity_prediction)


