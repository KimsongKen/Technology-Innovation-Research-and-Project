print("\n== 6. Disease Prediction Smoke Test ==")

def test_disease_prediction():
    import joblib, numpy as np, catboost as cb, re
    import pandas as pd

    le        = joblib.load(os.path.join(MODELS_DIR, "label_encoder.pkl"))
    model_lgb = joblib.load(os.path.join(MODELS_DIR, "lightgbm_model.pkl"))
    model_cb  = cb.CatBoostClassifier()
    model_cb.load_model(os.path.join(MODELS_DIR, "catboost_model.cbm"))

    df = pd.read_csv(os.path.join(DATA_DIR, "saca_final_dataset_self.csv"))

    # Replicate rare class grouping from 02_disease_prediction.py
    y = df["diseases"]
    disease_counts = y.value_counts()
    rare_diseases  = disease_counts[disease_counts < 10].index
    df["diseases"] = y.replace(rare_diseases, "Other_Rare_Disease")

    X = df.drop(["diseases", "Severity"], axis=1)

    def clean_feature_names(columns):
        cleaned = []
        seen = set()
        for col in columns:
            c = re.sub(r"[^A-Za-z0-9_]+", "_", col).strip("_").lower()
            if not c: c = "feature"
            orig, i = c, 1
            while c in seen:
                c = f"{orig}_{i}"; i += 1
            seen.add(c)
            cleaned.append(c)
        return cleaned

    X.columns = clean_feature_names(list(X.columns))
    sample    = X.iloc[[0]]

    lgb_prob = np.asarray(model_lgb.predict(sample))
    lgb_pred = le.inverse_transform([np.argmax(lgb_prob)])[0]
    cb_pred  = le.inverse_transform(model_cb.predict(sample).flatten())[0]

    print(f"         LightGBM → {lgb_pred} | CatBoost → {cb_pred}", end=" ")
    assert isinstance(lgb_pred, str), "LGB prediction not a string"
    assert isinstance(cb_pred,  str), "CB  prediction not a string"

test("Disease models predict correctly", test_disease_prediction)


